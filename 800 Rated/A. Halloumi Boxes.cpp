#include <bits/stdc++.h>
using namespace std;

int main() 
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;

    while(t--)
    {
        int n, k;
        cin >> n >> k;

        vector<int> arr(n);
        for(int i = 0; i < n; i++) cin >> arr[i];

        bool isSorted = true;
        for(int i = 1; i < n; i++)
        {
            if(arr[i-1] > arr[i])
            {
                isSorted = false;
                break;
            }
        }

        if(isSorted || k > 1)
            cout << "YES\n";
        else
            cout << "NO\n";
    }

    return 0;
}
